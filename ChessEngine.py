class GameState:
    def __init__(self):
        # Tabuleiro com uma matriz de 8x8
        # O primeiro caractere é a cor (b=black, w=white)
        # O segundo é a peça (R=Torre, N=Cavalo, B=Bispo, Q=Rainha, K=Rei, P=Peão)
        self.board = [
            ["bR", "bN", "bB", "bQ", "bK", "bB", "bN", "bR"],
            ["bP", "bP", "bP", "bP", "bP", "bP", "bP", "bP"],
            ["--", "--", "--", "--", "--", "--", "--", "--"],
            ["--", "--", "--", "--", "--", "--", "--", "--"],
            ["--", "--", "--", "--", "--", "--", "--", "--"],
            ["--", "--", "--", "--", "--", "--", "--", "--"],
            ["wP", "wP", "wP", "wP", "wP", "wP", "wP", "wP"],
            ["wR", "wN", "wB", "wQ", "wK", "wB", "wN", "wR"]
        ]
        self.white_to_move = True
        self.move_log = [] # Corrigido de move_long para move_log (padrão)

    def makeMove(self, move):
        self.board[move.start_row][move.start_col] = "--"
        self.board[move.end_row][move.end_col] = move.piece_moved
        self.move_log.append(move) # registra o log
        self.white_to_move = not self.white_to_move # troca o turno

